# Determine what 10 devided by 3 is.
print(10/3)

# Determine how many times 3 fits into 10 without the remainder.
print(10//3)

# Dtermin what is left over after determining how many times 3 can go into 10.
print(10%3)

# Use an exponent to determine the value of 10 to the power of 3.
print(10**3)

# Use an augmented assignment operator to increment a value with less steps.
x = 10
x += 3
print(x)
