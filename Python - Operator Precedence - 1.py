# Order of Operations: Parenthesis > Exponentiation > (Multiplication or Division) > (Addition or Subraction)
# Example: First (6 - 4 = 2) Second (2 ** 2 = 4) Third (3 * 4 = 12) Fourth (10 + 12 = 22) Fifth (22 + 2 = 24)

