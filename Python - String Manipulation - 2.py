# Define a variable
course = 'Python is VERY good'

# Print course variable content.
print(course)

# Use upper method to make all letters in the course variable uppercase.
print(course.upper())

# Use lower method to make all letters in the course variable lowercase.
print(course.lower())

# Use the find method to determine where the letter o is in the course variable.
print(course.find('o'))

# Use the replace method to replace good with bad.
print(course.replace('good', 'bad'))

# Determine if the string VERY is contained in the course variable.
print('VERY' in course)
