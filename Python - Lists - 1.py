# Lists

names = ['John', 'Bob', 'Mike', 'Sarah', 'Mary']

# This will print the names list exactly how it looks.
print(names)

for name in names:
    i = name
    print(i)

# This will print the first item from list names.    
print(names[0])

# This will print the 2nd to the 4th item in the list.
print(names[2:4])
