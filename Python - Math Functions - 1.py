# Math Functions

# Import the math module to use functions within it.
import math

# Use the round function to round a number up.
x = 2.9
print(round(x))

# Use the absolute (abs) function to determine the absolute value of a number.
print(abs(-2.9))

# From the math module use the ceiling (ceil) method to find the next whole number of a fraction.
print(math.ceil(3.8))

# From the math module use the floor (floor) method to find the previous whole number of a fraction.
print(math.floor(3.8))
